package StringAssignmentTwo;

public class QuestionThree {
    // How do you find the length of a string in Java? Explain with an example
    public static void main(String[] args) {
        // Answer : we use the length() method in String class to find
        // length of a string

        String string = "Java Programming";
        System.out.println("Length of string : " + string.length());
        // as "Java Programming" has 16 character it will show length
        // of string as 16
    }
}
